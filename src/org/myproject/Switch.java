package org.myproject;

public class Switch {
	public static void main(String[] args) {

		int a = 5;
		switch (a) {
		case 1:
			System.out.println("vikey");
			break;
		case 2:
			System.out.println("vkj");
			break;
		case 3:
			System.out.println("jvk");
			break;
		case 4:
			System.out.println("viknesh");
			break;
		default:
			System.out.println("not match");
			break;
		}

	}
}
