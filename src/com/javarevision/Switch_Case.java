package com.javarevision;

public class Switch_Case
{
	public static void main(String []args)
	{
		int a=4;
		switch(a)
		{
		case 1:
			System.out.println("VK");
			break;
		case 2:
			System.out.println("VKJ");
			break;
		case 3:
			System.out.println("Vikey");
			break;
		case 4:
			System.out.println("VK47");
		break;
		default:
			System.out.println("cannot match");
		break;
		}
	}

}
