package com.javarevision;

public class Type_Casting 
{
	public static void main(String[] args) {
       //Widening[ convert small datatype into large datatype ]
		
		int i=10;
		double d=i;
		System.out.println(d);
		
		//Narrowing [ convert large datataype into small datatype ]
		
		double d1=46.987;
		int a=(int) d1;
		System.out.println(a);
		
	}
		
	
		
	

}
